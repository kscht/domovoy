def surql_str(s):
    return "'" + s.replace("'", "\\'") + "'"

def thing_id(name):
    return name.replace(" ", "_").replace("-", "_").replace("/", "_").lower()

def create_thing(tid, **fields):
    parts = []
    for k, v in fields.items():
        if v is None:
            continue
        if isinstance(v, bool):
            parts.append(f"{k} = {str(v).lower()}")
        elif isinstance(v, (int, float)):
            parts.append(f"{k} = {v}")
        elif isinstance(v, str) and (v.startswith("thing:") or v.startswith("time::")):
            parts.append(f"{k} = {v}")
        elif isinstance(v, list):
            items = ", ".join(surql_str(i) for i in v)
            parts.append(f"{k} = [{items}]")
        else:
            parts.append(f"{k} = {surql_str(str(v))}")
    body = ", ".join(parts)
    return f"CREATE thing:`{tid}` SET {body};"

def relate(from_id, edge, to_id, **fields):
    extra = ""
    if fields:
        parts = []
        for k, v in fields.items():
            if isinstance(v, (int, float)):
                parts.append(f"{k} = {v}")
            else:
                parts.append(f"{k} = {surql_str(str(v))}")
        extra = " SET " + ", ".join(parts)
    return f"RELATE thing:`{from_id}`->{edge}->thing:`{to_id}`{extra};"

