for (name, kind, location, extra_fields) in ITEMS:
    base = thing_id(name)
    if base in seen_names:
        seen_names[base] += 1
        tid = f"{base}_{seen_names[base]}"
    else:
        seen_names[base] = 0
        tid = base
    fields = {"kind": kind, "name": name, "created_at": NOW, **extra_fields}
    w(create_thing(tid, **fields))
    w(relate(location, "contains", tid))
    generated.append((tid, name, kind, location))

